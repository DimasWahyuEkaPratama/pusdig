     void getData() {
    table.setRowCount(0);

    try {
        String sql = "SELECT * FROM kategori";
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();

        while (rs.next()) {
            Object[] obj = new Object[3];
            obj[0] = rs.getInt("Kategori_Id");
            obj[1] = rs.getString("Name_Kategori");
            obj[2] = rs.getDate("Created_At");
            table.addRow(obj);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
     
     }
private void pilihKategori() {
    int row = table_kategori.getSelectedRow();
    if (row == -1) return;

    // ambil ID & NAMA
    int idKategori = Integer.parseInt(
        table_kategori.getValueAt(row, 0).toString()
    );
    String namaKategori = table_kategori.getValueAt(row, 1).toString();

    // kirim ke form buku
    formBuku.setKategori(idKategori, namaKategori);

    dialog.dispose();
}

    

